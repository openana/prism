# Collect valid URL prefixes for path generation.  Must match url_prefix
# values generated by _mirrors_yaml below (i.e. "/host1/alpine/", etc.).
ALL_VALID_PREFIXES = (
    [f"/host1/{name}/" for name, _ in MOCK_MIRRORS_HOST1]
    + [f"/host2/{name}/" for name, _ in MOCK_MIRRORS_HOST2]
)


def _mirrors_yaml(mirrors: list[tuple[str, str]], url_prefix_base: str) -> str:
    """Generate YAML lines for a list of (name, desc) mirror entries.

    Returns content with 0 base-indent so it can be indented uniformly by
    the caller via ``textwrap.indent``.
    """
    lines = []
    for name, desc in mirrors:
        lines.append(
            f"- name: {name}\n"
            f'  desc: "{desc}"\n'
            f"  type: rsync\n"
            f"  url_prefix: {url_prefix_base}/{name}/\n"
            f"  real_url_prefix: {url_prefix_base}/{name}/\n"
            f"  help:\n"
            f'    mode: "off"'
        )
    return "\n".join(lines)


