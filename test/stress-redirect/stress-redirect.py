#!/usr/bin/env python3
"""
Stress test for prism's redirect handler (GET /{path:*} → 301/404).

Orchestrates:
  1. prism built with -tags debug (pprof enabled)
  2. A test config.yaml with populated URL trie (dummy backends)
  3. Concurrent HTTP load against catch-all redirect paths
  4. Metrics collection (latency percentiles, throughput, errors)
  5. Clean shutdown + pprof file collection

Unlike stress-mirrors.py, this test needs NO mock services — the redirect
handler only uses the path resolver (trie) and the web handler (for 404s).
The trie is populated from config at startup.  The mirror manager and index
provider are initialized with unreachable endpoints and never called by the
redirect code path.

Usage:
  python3 test/stress-redirect/stress-redirect.py [--interval 30] [--concurrency 10]
"""

