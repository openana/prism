def rand_subpath(rng: random.Random, depth: int = 0) -> str:
    """Generate a random sub-path segment to append after a prefix."""
    import string

    if depth <= 0:
        depth = rng.randint(1, 4)
    parts = []
    for _ in range(depth):
        length = rng.randint(3, 12)
        segment = "".join(rng.choices(string.ascii_lowercase + string.digits, k=length))
        parts.append(segment)
    return "/".join(parts) + "/"


def rand_invalid_path(rng: random.Random) -> str:
    """Generate a path that is guaranteed NOT to match any registered prefix."""
    import string

    # Use prefixes that look like real ones but aren't in our mirror list.
    bogus = rng.choice([
        "random", "nonexistent", "fake", "invalid", "bogus",
        "testdata", "experimental", "staging", "legacy", "custom",
    ])
    length = rng.randint(3, 12)
    sub = "".join(rng.choices(string.ascii_lowercase + string.digits, k=length))
    return f"/{bogus}/{sub}/"


def generate_paths(
    valid_prefixes: list[str],
    valid_count: int,
    invalid_count: int,
    seed: int = 42,
) -> list[str]:
    """Generate a reproducible list of paths.

    *valid_count* paths start with a known prefix (→ 301 redirect expected).
    *invalid_count* paths do not match any prefix (→ 404 expected).
    """
    rng = random.Random(seed)
    paths: list[str] = []

    for _ in range(valid_count):
        prefix = rng.choice(valid_prefixes)
        sub = rand_subpath(rng)
        paths.append(prefix + sub)

    for _ in range(invalid_count):
        paths.append(rand_invalid_path(rng))

    rng.shuffle(paths)
    return paths


