def build_test_config() -> str:
    """Generate a test config.yaml as a string.

    The config has two hosts with 10 mirrors each, populating the URL trie
    with 20 prefixes.  tunasync and nginx endpoints point at an unreachable
    port — neither subsystem is used by the redirect handler.
    """
    template = textwrap.dedent("""\
    log:
      level: warn
      output: stderr

    access_log:
      level: error
      output: stderr

    http:
      listen: "{prism_listen}"
      proto_header: "X-Forwarded-Proto"
      tcp_keepalive: true

    index:
      cache_ttl: 5m
      cache_max_bytes: 64MB

    sync_status:
      cache_ttl: 60s
      fetch_timeout: 1s

    hosts:
    - name: host1
      fqdn: 127.0.0.1
      index:
        driver: nginx
        nginx:
          timeout: 1s
          base_url: http://127.0.0.1:1/api/index/
      sync_status:
        driver: tunasync
        ttl: 60s
        tunasync:
          endpoint: http://127.0.0.1:1/tunasync.json
          timeout: 1s
      mirrors:
    __HOST1_MIRRORS__
    - name: host2
      fqdn: 127.0.0.1
      index:
        driver: nginx
        nginx:
          timeout: 1s
          base_url: http://127.0.0.1:1/api/index/
      sync_status:
        driver: tunasync
        ttl: 60s
        tunasync:
          endpoint: http://127.0.0.1:1/tunasync.json
          timeout: 1s
      mirrors:
    __HOST2_MIRRORS__
    """)

    template = template.replace("{prism_listen}", DEFAULT_PRISM_LISTEN)

    # After dedent, ``mirrors:`` sits at 2-space indent under the host
    # block.  Mirror list items need 4-space indent (2 more).
    host1 = textwrap.indent(
        _mirrors_yaml(MOCK_MIRRORS_HOST1, "/host1"), "    "
    )
    host2 = textwrap.indent(
        _mirrors_yaml(MOCK_MIRRORS_HOST2, "/host2"), "    "
    )
    return template.replace("__HOST1_MIRRORS__", host1).replace(
        "__HOST2_MIRRORS__", host2
    )


