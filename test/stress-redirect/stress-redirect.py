# Main
# ---------------------------------------------------------------------------


def main() -> None:
    ts = timestamp()

    parser = argparse.ArgumentParser(
        description="Stress test prism's redirect handler (GET /{path:*})",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=textwrap.dedent("""\
            Examples:
              %(prog)s --interval 30 --concurrency 10
              %(prog)s --interval 60 --concurrency 50
              %(prog)s --interval 10 --concurrency 4 --output-dir ./results
              %(prog)s --interval 30 --concurrency 20 --valid-ratio 0.5
        """),
    )
    parser.add_argument(
        "--interval", "-t",
        type=int,
        default=DEFAULT_INTERVAL,
        help=f"Stress test duration in seconds (default: {DEFAULT_INTERVAL})",
    )
    parser.add_argument(
        "--concurrency", "-c",
        type=int,
        default=DEFAULT_CONCURRENCY,
        help=f"Number of concurrent workers (default: {DEFAULT_CONCURRENCY})",
    )
    parser.add_argument(
        "--prism-port",
        type=int,
        default=DEFAULT_PRISM_PORT,
        help=f"Port for prism (default: {DEFAULT_PRISM_PORT})",
    )
    parser.add_argument(
        "--valid-ratio", "-r",
        type=float,
        default=DEFAULT_VALID_RATIO,
        help=f"Fraction of paths that match a known prefix → 301 "
             f"(default: {DEFAULT_VALID_RATIO})",
    )
    parser.add_argument(
        "--path-count", "-n",
        type=int,
        default=DEFAULT_PATH_COUNT,
        help=f"Number of unique paths to generate (default: {DEFAULT_PATH_COUNT})",
    )
    parser.add_argument(
        "--output-dir", "-o",
        type=pathlib.Path,
        default=PROJECT_ROOT / f"stress-redirect-results-{ts}",
        help=f"Output directory for reports and profiles "
             f"(default: stress-redirect-results-<timestamp>)",
    )
    args = parser.parse_args()

    if not 0.0 < args.valid_ratio <= 1.0:
        print("ERROR: --valid-ratio must be in (0.0, 1.0]", file=sys.stderr)
        sys.exit(1)

    runner = StressRunner(
        interval=args.interval,
        concurrency=args.concurrency,
        prism_port=args.prism_port,
        output_dir=args.output_dir,
        valid_ratio=args.valid_ratio,
        path_count=args.path_count,
    )

    # Handle Ctrl-C gracefully during setup
    def _sig_handler(signum, frame):
        print("\nInterrupted during setup — cleaning up...")
        runner.teardown(collect_profiles=False)
        sys.exit(1)

    signal.signal(signal.SIGINT, _sig_handler)

    try:
        runner.setup()

        # Restore default SIGINT for stress phase (KeyboardInterrupt in run())
        signal.signal(signal.SIGINT, signal.SIG_DFL)

        metrics = runner.run()
        runner.report(metrics)
    except Exception as e:
        print(f"\nERROR: {e}", file=sys.stderr)
        sys.exit(1)
    finally:
        runner.teardown()


if __name__ == "__main__":
    main()
