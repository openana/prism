def http_get_no_redirect(url: str, timeout: float = 5.0) -> tuple[int, float]:
    """Perform an HTTP GET **without following redirects**.

    Returns (status_code, elapsed_seconds).  This is critical for the
    redirect stress test because the redirect target (host FQDN) points to
    an unreachable address (127.0.0.1 with no server on port 80).  Following
    the 301 would always result in a connection error instead of measuring
    the redirect latency.
    """
    start = time.monotonic()

    class NoRedirect(urllib.request.HTTPRedirectHandler):
        def redirect_request(self, req, fp, code, msg, headers, newurl):
            return None

    opener = urllib.request.build_opener(NoRedirect())
    try:
        with opener.open(url, timeout=timeout) as resp:
            _ = resp.read()
            return resp.status, time.monotonic() - start
    except urllib.error.HTTPError as e:
        # e.code is set when the server returns a response; for 301/404 we
        # still get here because the NoRedirect handler doesn't redirect.
        return e.code, time.monotonic() - start
    except Exception:
        return 0, time.monotonic() - start


def health_check(
    url: str,
    timeout: float = 2.0,
    retries: int = 30,
    interval: float = 0.5,
) -> bool:
    """Poll a URL until it responds 200 or retries exhausted."""
    for _ in range(retries):
        try:
            with urllib.request.urlopen(url, timeout=timeout) as resp:
                if resp.status == 200:
                    return True
        except Exception:
            pass
        time.sleep(interval)
    return False


def kill_process(proc: subprocess.Popen, name: str) -> None:
    """Gracefully terminate a process, then force-kill if needed."""
    if proc is None or proc.poll() is not None:
        return
    print(f"  Stopping {name} (pid={proc.pid})...")
    proc.terminate()
    try:
        proc.wait(timeout=5)
    except subprocess.TimeoutExpired:
        print(f"  Force-killing {name}...")
        proc.kill()
        proc.wait()


def timestamp() -> str:
    """Return a compact ISO-8601 timestamp for directory naming."""
    return datetime.datetime.now().strftime("%Y%m%dT%H%M%S")


